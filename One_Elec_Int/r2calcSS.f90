subroutine RRnanoncon(naRR,olRR,RR,noRR,noatoms,invintatomdist)
 implicit none
 integer, intent(in)           :: noatoms,  noRR                       
 real*8, intent(in)            :: RR(1:30,1:noRR) , OLRR(1:noRR,1:4), invintatomdist(1:noatoms,1:noatoms)           
 real*8, intent(out)           :: naRR(1:noRR,1:4,1:noatoms) 
 integer                       :: i, atid, atcentre1

!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
! Calculates (S|V|S) for non-concentric nuclei
! Method is multipole moment interactions
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
! This program takes inputs: 
! noRR (number SS shell pairs)
! RR  (data for SS shell pairs)
! 
! and outputs
!  NARR (nuclear attraction integrals for SS shell pairs) 
!  each entry for a SP data in KERR has 4 parts (integral, shellid1, shellid2, normalisation constant for SP)
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!

 naRR = 0d0;

do atid=1,noatoms
 naRR(1:noRR,2,atid) = RR(1,1:noRR)
 naRR(1:noRR,3,atid) = RR(2,1:noRR)
 naRR(1:noRR,4,atid) = RR(6,1:noRR)
end do

do i=1,noRR
 atcentre1 = int(RR(3,i))
 do atid=1,noatoms
   naRR(i,1,atid) = OLRR(i,1)*invintatomdist(atcentre1,atid)
 end do
end do

return
end subroutine RRnanoncon
