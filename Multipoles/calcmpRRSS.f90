  integer, intent(in)   :: noRR, noatoms, nLpure 
  real*8, intent(in)    :: RRr(1:30,1:noRR), atomsr(1:5,1:noatoms)
  real*8, intent(out)   ::  mpRRSS(1:noRR,1:nLpure+4)
  real*8                :: OLRRtab(6:60)
  integer               :: i, n
  
  mpRRSS = 0d0; 
  
  OLRRtab(6) = 0.04986655005698085d0
  OLRRtab(7) =0.03490658503988659d0
  OLRRtab(8) =0.02538660730173570d0
