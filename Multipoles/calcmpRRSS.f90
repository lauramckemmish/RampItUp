  OLRRtab(49) =0.0001895380183161263d0
  OLRRtab(50) =0.0001788094512416286d0
  OLRRtab(51) =0.0001688755928393159d0
  OLRRtab(52) =0.0001596641968662623d0
  OLRRtab(53) =0.0001511107577484268d0
