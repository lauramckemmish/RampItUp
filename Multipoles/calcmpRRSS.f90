  OLRRtab(13) =0.007479982508547127d0
  OLRRtab(14) =0.006159985595274104d0
  OLRRtab(15) =0.005133321329395087d0
  OLRRtab(16) =0.004322796908964284d0
  OLRRtab(17) =0.003674377372619641d0
  OLRRtab(18) =0.003149466319388264d0
  OLRRtab(19) =0.002719993639471682d0
  OLRRtab(20) =0.002365211860410159d0
  OLRRtab(21) =0.002069560377858889d0
  OLRRtab(22) =0.001821213132515822d0
  OLRRtab(23) =0.001611073155687073d0
  OLRRtab(24) =0.001432065027277399d0
  OLRRtab(25) =0.001278629488640534d0
  OLRRtab(26) =0.001146357472574272d0
  OLRRtab(27) =0.001031721725316845d0
  OLRRtab(28) =0.0009318776873829568d0
