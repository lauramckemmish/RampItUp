  OLRRtab(59) =0.0001107559546479744d0
  OLRRtab(60) =0.0001054818615694994d0
  
  do i=1,noRR
     n = int(RRr(4,i)+RRr(5,i))
     mpRRSS(i,1)   = OLRRtab(n)/(4*dacos(-1d0))   ! Charge of Shell pair.
     mpRRSS(i,nLpure+1) = RRr(1,i)    ! Shell ID2
     mpRRSS(i,nLpure+2) = RRr(2,i)    ! Shell ID2
     mpRRSS(i,nLpure+3) = RRr(6,i)    ! Normalisation constant
     mpRRSS(i,nLpure+4) = RRr(3,i)    ! Ramp center
  end do
  
  
  return
end subroutine calcmpRRSS
